"""
Amanflix Production Update Helper
==================================

Automates production version updates by swapping code folders while preserving
the virtual environment and data configuration.

Usage:
    python update_helper.py configure       - Set up paths interactively
    python update_helper.py update          - Perform a version update
    python update_helper.py update --dry-run - Show what update would do
    python update_helper.py status          - Show current config and system state
    python update_helper.py rollback        - Restore from a backup
    python update_helper.py clean           - Delete old backups

Expected folder structure:
    Z:\\Amanflix\\
    ├── Data\\                  (permanent data - uploads, posters, db, etc.)
    ├── Amanflix_prod\\         (current production code)
    ├── Amanflix_new_version\\  (downloaded new version)
    ├── Amanflix_backups\\      (archived old versions)
    ├── update_helper.py       (this script)
    └── update_config.json     (generated by 'configure')
"""

import os
import sys
import json
import shutil
import subprocess
import signal
import time
from datetime import datetime


SCRIPT_DIR = os.path.dirname(os.path.abspath(__file__))
CONFIG_FILE = os.path.join(SCRIPT_DIR, 'update_config.json')


# ─── Helpers ─────────────────────────────────────────────────────────────────

def load_config():
    if not os.path.exists(CONFIG_FILE):
        print("[ERROR] update_config.json not found. Run 'python update_helper.py configure' first.")
        sys.exit(1)
    with open(CONFIG_FILE, 'r') as f:
        return json.load(f)


def save_config(config):
    with open(CONFIG_FILE, 'w') as f:
        json.dump(config, f, indent=4)
    print(f"[OK] Config saved to {CONFIG_FILE}")


def resolve_path(path):
    """Resolve a path relative to the script directory."""
    if os.path.isabs(path):
        return os.path.normpath(path)
    return os.path.normpath(os.path.join(SCRIPT_DIR, path))


def find_app_process():
    """Find running app.py process. Returns list of PIDs."""
    pids = []
    try:
        if sys.platform == 'win32':
            result = subprocess.run(
                ['wmic', 'process', 'where',
                 "commandline like '%app.py%' and name like '%python%'",
                 'get', 'processid'],
                capture_output=True, text=True, timeout=10
            )
            for line in result.stdout.strip().split('\n'):
                line = line.strip()
                if line.isdigit():
                    pids.append(int(line))
        else:
            result = subprocess.run(
                ['pgrep', '-f', 'app.py'],
                capture_output=True, text=True, timeout=10
            )
            for line in result.stdout.strip().split('\n'):
                line = line.strip()
                if line.isdigit():
                    pids.append(int(line))
    except Exception as e:
        print(f"[WARN] Could not check for running processes: {e}")
    return pids


def stop_app(config):
    """Stop the running app.py process."""
    pids = find_app_process()
    if not pids:
        print("[INFO] No running app.py process found.")
        return True

    print(f"[INFO] Found app.py process(es): {pids}")
    for pid in pids:
        try:
            if sys.platform == 'win32':
                subprocess.run(['taskkill', '/F', '/PID', str(pid)],
                             capture_output=True, timeout=10)
            else:
                os.kill(pid, signal.SIGTERM)
            print(f"[OK] Stopped process {pid}")
        except Exception as e:
            print(f"[WARN] Could not stop process {pid}: {e}")

    # Wait for processes to exit
    time.sleep(2)

    # Verify they stopped
    remaining = find_app_process()
    if remaining:
        print(f"[WARN] Some processes still running: {remaining}")
        return False
    print("[OK] All app.py processes stopped.")
    return True


def start_app(config):
    """Start app.py using the venv's Python interpreter."""
    prod_path = resolve_path(config['prod_folder'])
    api_path = os.path.join(prod_path, 'api')
    venv_rel = config.get('venv_path', 'api/venv')
    venv_path = os.path.join(prod_path, venv_rel)

    if sys.platform == 'win32':
        python_exe = os.path.join(venv_path, 'Scripts', 'python.exe')
    else:
        python_exe = os.path.join(venv_path, 'bin', 'python')

    if not os.path.exists(python_exe):
        print(f"[ERROR] Python executable not found at {python_exe}")
        print("[INFO] You may need to start the app manually.")
        return False

    app_py = os.path.join(api_path, 'app.py')
    if not os.path.exists(app_py):
        print(f"[ERROR] app.py not found at {app_py}")
        return False

    print(f"[INFO] Starting app.py from {api_path}")
    try:
        if sys.platform == 'win32':
            subprocess.Popen(
                [python_exe, 'app.py'],
                cwd=api_path,
                creationflags=subprocess.CREATE_NEW_PROCESS_GROUP | subprocess.DETACHED_PROCESS
            )
        else:
            subprocess.Popen(
                [python_exe, 'app.py'],
                cwd=api_path,
                start_new_session=True,
                stdout=subprocess.DEVNULL,
                stderr=subprocess.DEVNULL
            )
        print("[OK] app.py started successfully.")
        return True
    except Exception as e:
        print(f"[ERROR] Failed to start app.py: {e}")
        return False


def generate_data_config(config):
    """Generate data_config.json content for the app."""
    return {
        "data_root": config['data_root_relative']
    }


